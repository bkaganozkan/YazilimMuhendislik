package gorev;

public class Gorevler {
	
	private Integer id;
	private String baslik;
	private String gorevtext;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getBaslik() {
		return baslik;
	}
	public void setBaslik(String baslik) {
		this.baslik = baslik;
	}
	public String getGorevtext() {
		return gorevtext;
	}
	public void setGorevtext(String gorevtext) {
		this.gorevtext = gorevtext;
	}
	
	

}
